package DataHiding;

public class Employee {
		
	private String name;
	private int id;
	
	public  void setName(String m_name )
	{
		name=m_name;
		
	}
	public String getName()
	{
		return name;
	}
	
	public void setId(int m_id)
	
	{
		id=m_id;	
	}
	public int getId()
	{
		return id;
	}
}
